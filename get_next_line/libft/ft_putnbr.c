#include "libft.h"

void	ft_putnbr(int n)
{
	char nb;

	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	nb = (char)(48 + n % 10);
	write(1, &nb, 1);
}
