#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char nb;

	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	nb = (char)(48 + n % 10);
	write(fd, &nb, 1);
}
